const ScrollPath = ({ d, index, total, progress }) => {
  // We are drawing between 35% (0.35) and 100% (1.0) of the scroll
  const drawStart = 0.35;
  const drawEnd = 1.0;
  
  // Calculate the specific time slot for this single path within that range
  const totalDrawTime = drawEnd - drawStart;
  const step = totalDrawTime / total;
  
  const start = drawStart + (index * step);
  const end = start + step + 0.05; // 0.05 overlap for smoothness

  const pathLength = useTransform(progress, [start, end], [0, 1]);
  const opacity = useTransform(progress, [start, start + 0.01], [0, 1]);

  return <motion.path d={d} style={{ pathLength, opacity }} />;
};

